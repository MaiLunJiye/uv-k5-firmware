void UI_Display_ZH_CN(ZH_CN_Index ch, const uint8_t x, const uint8_t line) {
    const uint8_t *bitfont = g_font_ZH_CN[ch];
	int i = 0;
	for (i = 0; i < FONT_ZH_CN_WIDTH; i++) {
		gFrameBuffer[line][x + i] = bitfont[i];

		uint8_t down_idx = i / 2 + FONT_ZH_CN_WIDTH;
		if (i % 2 == 0) {
			gFrameBuffer[line + 1][x + i] = bitfont[down_idx] >> 4;
		} else {
			gFrameBuffer[line + 1][x + i] = bitfont[down_idx] & 0x0F;
		}
	}
}

